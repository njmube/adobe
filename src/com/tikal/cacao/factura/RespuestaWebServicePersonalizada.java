public class RespuestaWebServicePersonalizada {

	private String mensajeRespuesta;
	
	private String uuidPrefactura;
	
	private String uuidFactura;

	public String getMensajeRespuesta() {
		return mensajeRespuesta;
	}

	public void setMensajeRespuesta(String mensajeRespuesta) {
		this.mensajeRespuesta = mensajeRespuesta;
	}

	public String getUuidPrefactura() {
		return uuidPrefactura;
	}

	public void setUuidPrefactura(String uuidPrefactura) {
		this.uuidPrefactura = uuidPrefactura;
	}

	public String getUuidFactura() {
		return uuidFactura;
	}

	public void setUuidFactura(String uuidFactura) {
		this.uuidFactura = uuidFactura;
	}
	
}
